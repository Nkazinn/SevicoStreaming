    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
       
        if(!ValidacaoDados()){
            return; 
        }
       UserDao dao = new UserDao();
       Usuario user = new Usuario();
       user.setNome(txtNome.getText());
       user.setEmail(txtEmail.getText());
       user.setSenha(Criptografia.getMD5(txtSenha.getText()));
       String Confirmar = txtConfirmarSenha.getText();
       
        if (!Confirmar.equals(txtSenha.getText())) {

            JOptionPane.showMessageDialog(null, "⚠ As senhas não coincidem. Por favor, tente novamente.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;  // Interrompe o processo se as senhas não coincidirem
        }
        
        int status = dao.cadastrar(user);
       
        if (status > 0) {
        txtEmail.setText("");
        txtSenha.setText("");
        txtConfirmarSenha.setText("");
        txtNome.setText("");
        JOptionPane.showMessageDialog(null, "✅ Usuário cadastrado com sucesso!", "Sucesso",
        JOptionPane.INFORMATION_MESSAGE);
       }
        else{  
            JOptionPane.showMessageDialog(null, "❌ Erro ao cadastrar usuário.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        
        dao.desconectar();
    }//GEN-LAST:event_btnCadastrarActionPerformed

