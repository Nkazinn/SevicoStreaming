    private boolean ValidacaoDados() {

    boolean emailValido = txtEmail.getText().matches("^[\\w.-]+@[a-zA-Z\\d.-]+\\.[a-zA-Z]{2,}$");

    try {
       
        if (txtNome.getText().isEmpty() ||
            txtEmail.getText().isEmpty() ||
            String.valueOf(txtSenha.getPassword()).isEmpty() ||
            String.valueOf(txtConfirmarSenha.getPassword()).isEmpty()) {

            JOptionPane.showMessageDialog(null, "⚠ Preencha todos os campos.", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (!txtNome.getText().matches("[a-zA-Z0-9_]+")) {
            JOptionPane.showMessageDialog(null, "⚠ O nome de usuário só pode conter letras, números e _.","Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (!emailValido) {
            JOptionPane.showMessageDialog(null, "⚠ Email inválido! Use um formato válido (exemplo@dominio.com).", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        if (String.valueOf(txtSenha.getPassword()).length() < 4) {
            JOptionPane.showMessageDialog(null, "⚠ A senha deve ter no mínimo 4 caracteres.", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (!String.valueOf(txtSenha.getPassword()).equals(String.valueOf(txtConfirmarSenha.getPassword()))) {
            JOptionPane.showMessageDialog(null, "⚠ As senhas não coincidem!", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Ocorreu um erro: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    return true;
}

