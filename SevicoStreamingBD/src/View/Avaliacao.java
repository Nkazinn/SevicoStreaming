    public void configurarBotoesPorTipoUsuario(Usuario u) {
    
    if (u.getTipo().equalsIgnoreCase("Administrador")) {
        btnCadastro.setEnabled(true);
    } else if (u.getTipo().equalsIgnoreCase("usuario")) {
        btnCadastro.setEnabled(false);
        btnCadastro.setVisible(false);
    } else {
       
        btnCadastro.setVisible(false);
        btnCadastro.setEnabled(false);
    }
 }
    
    private boolean ValidacaoDados() {
         
       boolean notaB = txtNota.getText().matches("[0-5]");
         
        try {
            if (txtNota.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "⚠ Preencha todos os campos.","Erro", JOptionPane.ERROR_MESSAGE);
                return false;
            }
            
            if (notaB == false) {
                javax.swing.JOptionPane.showMessageDialog(null, "⚠️ Nota só vai de 1 a 5 e não pode conter letras\n", "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
                return false;
            }
            

        } catch (Exception e) {
            // Tratamento genérico para outros erros inesperados
            javax.swing.JOptionPane.showMessageDialog(this, "Ocorreu um erro: " + e.getMessage(), "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
            return false;
        }

        return true;
    }
    
    private boolean ValidacaoID() {
    try {
        // Verifica se o campo está vazio
        if (txtId.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "⚠ O campo ID não pode estar vazio.", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        // Verifica se o ID contém apenas números
        try {
            int id = Integer.parseInt(txtId.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "⚠ O ID deve conter apenas números.", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Ocorreu um erro: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    return true;
}

