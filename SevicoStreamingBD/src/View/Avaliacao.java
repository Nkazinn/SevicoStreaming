    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
       if(!ValidacaoID()) {
         return;   
       }
        
       int idPesquisa = Integer.parseInt(txtId.getText());
        
        AvaliarDao dao = new AvaliarDao();
        Conteudo conteudo = dao.getConteudoId(idPesquisa);
        
        if(conteudo==null){
            JOptionPane.showMessageDialog(null,"Conteúdo não encontrado");
        }
        else{
            txtId2.setText(String.valueOf(conteudo.getId()));
            txtNome.setText(conteudo.getTitulo());
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

