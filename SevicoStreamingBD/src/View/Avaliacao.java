    private void btnAvaliarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAvaliarActionPerformed
        if (!ValidacaoDados()) {
        return;  
        }
        
        int id = Integer.parseInt(txtId.getText());
        int nota = Integer.parseInt(txtNota.getText());
        
        UserDao userDao = new UserDao();
        CadastrarDao cadastrarDao = new CadastrarDao();
        AvaliarDao dao = new AvaliarDao();
        
        int idUsuario = usuario.getId();
        
        Usuario usuario = userDao.buscarPorId(idUsuario);
        Conteudo conteudo = cadastrarDao.buscarPorId(id);
        
        Avaliacaom avaliacaom = new Avaliacaom();
        
        avaliacaom.setNota(nota);
        avaliacaom.setConteudo(conteudo);
        avaliacaom.setUsuario(usuario);
        
        dao.atualizar(avaliacaom);
        
        JOptionPane.showMessageDialog(null,"Atualização feita com sucesso");
        
        txtId.setText("");
        txtId2.setText("");
        txtNome.setText("");
        txtNota.setText("");
        
    }//GEN-LAST:event_btnAvaliarActionPerformed

