    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
       
        if(txtSugestoes.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"⚠ Nada escrito no campo de sugestões","Erro", JOptionPane.ERROR_MESSAGE);
            return ;
        }
        
        SugestaoDao dao = new SugestaoDao();
        Conexao c = new Conexao();
        java.sql.Connection status = c.conectar();
       
        if (status == null) {
            JOptionPane.showMessageDialog(null, "Erro de conexão");
        } 
        
        dao.addSugestao(usuario.getId(),usuario.getNome(),txtSugestoes.getText());
        atualizarTabela();
        
        txtSugestoes.setText("");
    }//GEN-LAST:event_btnEnviarActionPerformed

