    private void atualizarTabela() {
      
        model.setRowCount(0);
        SugestaoDao dao = new SugestaoDao();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss");

        // Recuperando as sugestões e adicionando na tabela
        for (Sugestao s : dao.getSugestoes()) {
            model.addRow(new Object[]{
                s.getId(), 
                s.getNome(), 
                s.getDataHora().format(formatter), 
                s.getDescricao()
            });
        }
    }
    
    public void configurarBotoesPorTipoUsuario(Usuario u) {
    
    if (u.getTipo().equalsIgnoreCase("Administrador")) {
        btnCadastro.setEnabled(true);
    } else if (u.getTipo().equalsIgnoreCase("usuario")) {
        btnCadastro.setEnabled(false);
        btnCadastro.setVisible(false);
    } else {
       
        btnCadastro.setVisible(false);
        btnCadastro.setEnabled(false);
    }
 }
