    private void btnLogin1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogin1ActionPerformed
       
          if (!ValidacaoDados()) {
            return;  
        }
          
        UserDao dao = new UserDao();
        Usuario u = new Usuario();
        Conexao c = new Conexao();
        java.sql.Connection status = c.conectar();
       
        if (status == null) {
            JOptionPane.showMessageDialog(null, "Erro de conexão");
        }
        
        u.setEmail(txtEmail.getText().trim());
        u.setSenha(Criptografia.getMD5(txtSenha.getText().trim()));
        
        u = dao.validar(u);
        
        if(u!=null){
            
            JOptionPane.showMessageDialog(null, "Olá usuário, sua permissão é de " + u.getTipo() + ". Seja bem-vindo!", "Sucesso",
                    JOptionPane.INFORMATION_MESSAGE);
            
            if (u.getTipo().equalsIgnoreCase("Administrador")) {
                CadastrarS cadastrar = new CadastrarS(u);
                cadastrar.setVisible(true);
                this.dispose();
            } else {
                Home home = new Home(u);
                home.setVisible(true);
                this.dispose();
            }
            txtEmail.setText("");
            txtSenha.setText("");
        } else {
            JOptionPane.showMessageDialog(null,"⚠ Usuário não encontrado.Login ou senha estão errados.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }  
        
        dao.desconectar();
        
    }//GEN-LAST:event_btnLogin1ActionPerformed

