public void configurarBotoesPorTipoUsuario(Usuario u) {
    
    if (u.getTipo().equalsIgnoreCase("Administrador")) {
        btnCadastro.setEnabled(true);
    } else if (u.getTipo().equalsIgnoreCase("usuario")) {
        btnCadastro.setEnabled(false);
        btnCadastro.setVisible(false);
    } else {
       
        btnCadastro.setVisible(false);
        btnCadastro.setEnabled(false);
    }
 }

private boolean ValidacaoDados() {

    // Expressões regulares para validar formatos
    boolean validadeB = txtValidade.getText().matches("[0-9]{2}/[0-9]{4}");
    boolean cpfB = txtCpf.getText().matches("\\d{11}|\\d{3}\\.\\d{3}\\.\\d{3}-\\d{2}");
    boolean telefoneB = txtTelefone.getText().matches("\\+55\\s?\\d{2}\\s?\\d{5}-\\d{4}");
    boolean dataNascimentoB = txtData.getText().matches("\\d{2}/\\d{2}/\\d{4}");

    try {
        // Verifica se todos os campos foram preenchidos
        if (txtNumero.getText().isEmpty() ||
            txtValidade.getText().isEmpty() ||
            txtCodigo.getText().isEmpty() ||
            txtNome.getText().isEmpty() ||
            txtCpf.getText().isEmpty() ||
            txtTelefone.getText().isEmpty() ||
            txtData.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "⚠ Preencha todos os campos.", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        if (!txtNumero.getText().matches("\\d{1,19}")) {
            JOptionPane.showMessageDialog(null, "⚠ Número do cartão inválido! Deve conter apenas números (1 a 19 dígitos).", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (!txtCodigo.getText().matches("\\d{3,4}")) {
            JOptionPane.showMessageDialog(null, "⚠ Código de segurança inválido! Deve conter apenas números (3 ou 4 dígitos).", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (!validadeB) {
            JOptionPane.showMessageDialog(null, "⚠ Validade deve estar no formato MM/AAAA.", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        } else {
            String[] partes = txtValidade.getText().split("/");
            int mes = Integer.parseInt(partes[0]);
            int ano = Integer.parseInt(partes[1]);

            if (mes < 1 || mes > 12) {
                JOptionPane.showMessageDialog(null, "⚠ Mês inválido! Deve estar entre 01 e 12.", "Erro", JOptionPane.ERROR_MESSAGE);
                return false;
            }
        }
        
        if (!cpfB) {
            JOptionPane.showMessageDialog(null, "⚠ CPF inválido! Deve estar no formato 000.000.000-00 ou apenas números.", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        if (!telefoneB) {
            JOptionPane.showMessageDialog(null, "⚠ Telefone inválido! Deve ser no formato +55 11 94400-5908.", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        if (!dataNascimentoB) {
            JOptionPane.showMessageDialog(null, "⚠ Data de nascimento inválida! Deve estar no formato DD/MM/AAAA.", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Ocorreu um erro: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    return true;
 }
    
