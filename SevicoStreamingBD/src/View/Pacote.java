    private void btnComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarActionPerformed
    if (!ValidacaoDados()) {
        return;  
        }    
        
    PacoteDao pDao = new PacoteDao();
    UserDao dao = new UserDao();
    Conexao c = new Conexao();
             
    java.sql.Connection status = c.conectar();  
    
    if (status == null) {
            JOptionPane.showMessageDialog(null, "Erro de conexão");
        }
        
    String pacoteSelecionado = cboxPacote.getSelectedItem().toString();
    
    pacote = pDao.buscarPacotePorNome(pacoteSelecionado);

    if (pacote == null) {
        JOptionPane.showMessageDialog(null, "Pacote não encontrado!");
        return;
    }

    int resposta = JOptionPane.showConfirmDialog(
    null, 
    "O preço do " + pacote.getNome() + " é R$ " + pacote.getValor() + ".\nDeseja realmente comprar?", 
    "Confirmação de Compra", 
    JOptionPane.YES_NO_OPTION
 );
    
    if (resposta == JOptionPane.YES_OPTION) { 
        
    int idUsuario = usuario.getId();
    Usuario usuarioRecuperado = dao.buscarPorId(idUsuario);
    
    if (usuarioRecuperado == null) {
    JOptionPane.showMessageDialog(null, "Usuário não encontrado. Insira um cartão apenas para usuários existentes.");
    return; // Interrompe a operação se o usuário não existir
}
        
    String numeroCartao = txtNumero.getText();
    String nomeTitular = txtNome.getText();
    String validade = txtValidade.getText();
    String codigoSeguranca = txtCodigo.getText();
    String pagamento = cboxPagamento.getSelectedItem().toString();
    
    usuario.setPossuiPacote("Sim");
    
    int idPacote = pacote.getId();
    PacoteM p = pDao.buscarPorId(idPacote);
    usuario.setPacote(p);

    // Criar objeto Cartao e PacoteCartao

    Cartao cartao = new Cartao();
    
    cartao.setNomeTitular(nomeTitular);
    cartao.setNumero(numeroCartao);
    cartao.setValidade(validade);
    cartao.setCodigoSeguranca(codigoSeguranca);
    cartao.setTipo(pagamento);
    cartao.setUsuario(usuarioRecuperado);
    
    CartaoDao cartaoDAO = new CartaoDao();

        int statusCartao = cartaoDAO.addCartao(cartao);
        boolean statusUsuario = dao.atualizar(pacote,usuarioRecuperado);
        
        if (statusCartao == 1 && statusUsuario) {
            JOptionPane.showMessageDialog(null, "Compra realizada com sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao processar a compra!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        
        } else {
            JOptionPane.showMessageDialog(null, "Compra cancelada.");
        }

        dao.desconectar();
    }//GEN-LAST:event_btnComprarActionPerformed

