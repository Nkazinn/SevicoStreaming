    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        if (!ValidacaoDados()) {
        return;  
        }
        
        UserDao dao2 = new UserDao();
        Conteudo conteudo = new Conteudo();
        CadastrarDao dao = new CadastrarDao();
        Conexao c = new Conexao();
        java.sql.Connection status = c.conectar();
        int resposta;
        
        conteudo.setTipoConteudo(cboxTipo.getSelectedItem().toString());
        conteudo.setTitulo(txtNome.getText());
        conteudo.setQuantiEpisodios(Integer.parseInt(txtEpisodios.getText()));
        conteudo.setDataLancamento(txtData.getText());
        conteudo.setAutor(txtAutor.getText());
        conteudo.setDescricao(txtDescricao.getText());
        
        if (status == null) {
            JOptionPane.showMessageDialog(null, "Erro de conexão");
        }  {
            resposta = dao.salvar(conteudo);
            if (resposta == 1) {
                JOptionPane.showMessageDialog(null, "Dados incluidos com sucesso");
            } else if (resposta == 1062) {
                JOptionPane.showMessageDialog(null, "Filme já foi cadastrada");
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao tentar inserir dados");
            }
            
            dao2.desconectar();
        } 
        
        txtEpisodios.setText("");
        txtData.setText("");
        txtAutor.setText("");
        txtNome.setText("");
        txtDescricao.setText("");
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

