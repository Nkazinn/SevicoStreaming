     private boolean ValidacaoDados() {
         
       boolean dataB = txtData.getText().matches("[0-9]{2}[/][0-9]{2}[/][0-9]{4}");
       String autorB = txtAutor.getText();
         
        try {
            if (txtNome.getText().isEmpty() || txtEpisodios.getText().isEmpty() || txtData.getText().isEmpty() || txtAutor.getText().isEmpty() || txtDescricao.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "⚠ Preencha todos os campos.","Erro", JOptionPane.ERROR_MESSAGE);
                return false;
            }
            
            try {
                int numeroEp = Integer.parseInt(txtEpisodios.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "⚠️ O Número de Episódios deve conter apenas números!", "Erro", JOptionPane.ERROR_MESSAGE);
                return false;
            }
            
            if (dataB == false) {
                javax.swing.JOptionPane.showMessageDialog(null, "⚠️ Data precisa ser no formato dd/mm/aaaa e não pode conter letras\n", "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
                return false;
            }
            
            if (autorB.matches(".*\\d.*")) {
                javax.swing.JOptionPane.showMessageDialog(null, "⚠ Nome do autor não pode conter números", "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
                return false;
            }
            

        } catch (Exception e) {
            // Tratamento genérico para outros erros inesperados
            javax.swing.JOptionPane.showMessageDialog(this, "Ocorreu um erro: " + e.getMessage(), "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
            return false;
        }

        return true;
    }
  
    public void configurarBotoesPorTipoUsuario(Usuario u) {
    
    if (u.getTipo().equalsIgnoreCase("Administrador")) {
        btnCadastro.setEnabled(true);
    } else if (u.getTipo().equalsIgnoreCase("usuario")) {
        btnCadastro.setEnabled(false);
        btnCadastro.setVisible(false);
    } else {
       
        btnCadastro.setVisible(false);
        btnCadastro.setEnabled(false);
    }
 } 
     
